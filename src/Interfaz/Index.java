    public Automovil obtenerInformacion() {
        Automovil automovil = new Automovil();
        automovil.setColor(txtColor.getText());
        try {
            automovil.setPuertas(Integer.parseInt(txtPuertas.getText()));
        } catch (Exception e) {
            automovil.setPuertas(0);
        }
        return automovil;
    }


