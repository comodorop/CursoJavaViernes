    public boolean validarCampos(Automovil auto) {
        boolean ok = false;
        if (auto.getColor().equals("")) {
            JOptionPane.showMessageDialog(null, "Se requiere un color");
        } else if (auto.getPuertas() == 0) {
            JOptionPane.showMessageDialog(null, "Se requiere numero de puertas");
        } else {
            ok = true;
        }
        return ok;
    }

    public void imprimirInformacion(Automovil auto) {
        textResultado.setText(auto.toString());
    }

    public void sacarDialogo(Automovil auto) {
        JOptionPane.showMessageDialog(null, auto);
    }

    
